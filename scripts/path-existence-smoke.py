#!/usr/bin/env python3
"""path-existence-smoke - assert every backtick-quoted path in operator-
facing docs resolves to a real file or directory.

Two iterations of the path-existence audit shape (commits 5e01954,
385c867) caught 7 broken file-path references across 9 files
(crypto/audit.py vs audit_log.py; services/swarm/session_guard.py
vs services/swarm/findevil_swarm/session_guard.py; goldens/...
vs goldens/.../expected-findings.json; etc.). Each was a doc
claiming a path exists that doesn't - a SANS judge or contributor
following the citation would hit a 404. This smoke locks the
audit so future contributors can't silently re-introduce the same
shape.

For each operator-facing doc in the SCAN_LIST, extract every
backtick-quoted string of the form ``path/with/at/least/one/slash``
and check that it resolves to a real file or directory. Resolution
is tried in order: relative to the doc's own dir; relative to repo
root; for service READMEs, also relative to the service's package
dir (e.g. services/agent/README.md mentioning ``crypto/audit_log.py``
should resolve at services/agent/findevil_agent/crypto/audit_log.py).

A defined ALLOW_PATTERNS list captures the 43-of-47 false-positive
shapes the manual audits had to skip past:
  * URLs (http://, https://, mailto:)
  * MCP wire-format identifiers (tools/list, tools/call)
  * Runtime user dirs (~/.claude/, ~/hayabusa/...)
  * Install paths in decision runbooks (/usr/bin/, /usr/share/)
  * Deferred-per-A2 paths (apps/web/, apps/mcp-widgets/)
  * Dropped-per-A2 modules deliberately quoted in docs
    (findevil_agent/cli.py + 4 siblings)
  * Future-tense fixture files (fixtures/sha256sums.txt etc -
    generated by scripts/fetch-fixtures.sh at first download)
  * Conventional shorthand (`.LOG1` / `.LOG2`)

Wall-clock: ~30ms (a few hundred regex matches + Path.exists()
calls). Wired into docker/l1-compose.yml after divergence-smoke
as the 8th L1 smoke.
"""

from __future__ import annotations

import re
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent

# Operator-facing docs to scan.  Discovery is glob-based for the
# canonical doc directories so new files dropped under docs/,
# docs/runbooks/, agent-config/, and services/*/README.md are
# auto-gated without a manual SCAN_LIST update.  Plus three
# explicit root-level entries (CLAUDE.md, README.md, QUICKSTART.md)
# that aren't under any of those dirs.  EXPLICIT_DOCS captures the
# load-bearing root docs; GLOB_PATTERNS captures the dir-scoped
# auto-discovery.
EXPLICIT_DOCS: tuple[str, ...] = (
    "CLAUDE.md",
    "README.md",
    "QUICKSTART.md",
)

GLOB_PATTERNS: tuple[str, ...] = (
    # Canonical analyst-facing deep-dives + DATASET.md.
    "docs/*.md",
    # Decision runbooks + ci-smoke-checklist.
    "docs/runbooks/*.md",
    # Runtime DFIR agent identity (loaded by Claude Code at session
    # start per CLAUDE.md "Agent investigation prompt").
    "agent-config/*.md",
    # Per-service READMEs (one per service/* subdir).
    "services/*/README.md",
)

# Specific files that match a GLOB but should be excluded - e.g.
# templates that envsubst-substitute placeholders at v-submit time
# and therefore have unresolved ${VAR} tokens that look path-shaped
# but aren't real broken refs.
GLOB_EXCLUDES: frozenset[str] = frozenset(
    {
        "docs/templates/devpost-readme.md",
    }
)

# Service README -> the package directory under it.  Used for the
# "package-relative" resolution attempt on service READMEs (e.g.
# services/agent/README.md mentioning ``crypto/audit_log.py``
# resolves at services/agent/findevil_agent/crypto/audit_log.py).
SERVICE_PACKAGE_DIRS: dict[str, str] = {
    "services/agent/README.md": "services/agent/findevil_agent",
    "services/agent_mcp/README.md": "services/agent_mcp/findevil_agent_mcp",
    "services/swarm/README.md": "services/swarm/findevil_swarm",
    "services/mcp/README.md": "services/mcp/src",
}

# Allow-listed path patterns that look like paths but aren't files
# in this repo.  A path matching ANY of these is skipped.
ALLOW_PATTERNS: tuple[re.Pattern[str], ...] = (
    # URLs.
    re.compile(r"^(https?|mailto|ftp|data):"),
    # Anchored URLs without scheme.
    re.compile(r"^//"),
    # Protocol-Sift example paths in CLAUDE.md's appendix.
    re.compile(r"^\./(analysis|exports)/"),
    # Runtime user dirs.
    re.compile(r"^~/"),
    # Install / system paths in decision runbooks + protocol-sift
    # appendix examples.
    re.compile(r"^/(usr|etc|var|opt|dev|proc|sys|home)/"),
    # MCP wire-format identifiers (tools/list, tools/call) -
    # JSON-RPC method names, not file paths.
    re.compile(r"^tools/(list|call)$"),
    # Deferred-per-Amendment-A2 paths (apps/web + apps/mcp-widgets
    # are scheduled as week-7 polish bonus, not on the critical
    # path - docs reference them as future surfaces).
    re.compile(r"^apps/(web|mcp-widgets)(/|$)"),
    # Dropped-per-Amendment-A2 modules deliberately quoted in
    # docs to document what was removed (the L0 amendment-a2-
    # guard fails CI on their return).
    re.compile(
        r"^(?:services/agent/)?findevil_agent/(graph|api|cli|"
        r"supervisor|specialists)\.py$"
    ),
    re.compile(r"^(?:services/agent/)?findevil_agent/specialists$"),
    # Future-tense fixture files - generated by
    # scripts/fetch-fixtures.sh at first download per Spec #3 §5
    # and scripts/fetch-nist-fixture.sh's provenance recording.
    re.compile(r"^fixtures/sha256sums\.txt$"),
    re.compile(r"^fixtures/single-evtx/PROVENANCE\.txt$"),
    re.compile(r"^goldens/sans-starter/"),
    # OTRF Security-Datasets external repo paths quoted in
    # DATASET.md to identify which subtrees ship the EVTX
    # fixtures we use - those paths exist in the OTRF repo,
    # not this one.
    re.compile(r"^datasets/atomic/(windows|linux)/"),
    # Documented-removed pre-A1 modules (LiteLLM/USD-cap code
    # was dropped per Amendment A1; the docs deliberately quote
    # the removed paths to document what was removed, same
    # pattern as the dropped-per-A2 entries above).
    re.compile(r"^services/swarm/budget\.py$"),
    # Windows event-log channel names that look like paths (e.g.
    # `Microsoft-Windows-PowerShell/Operational`).
    re.compile(r"^Microsoft-Windows-"),
    # GitHub Actions reference (actions/cache, actions/checkout).
    re.compile(r"^actions/[a-z-]+$"),
    # External tool plugin/rules dirs commonly cited but not
    # in this repo.
    re.compile(r"^(?:[a-z][a-z0-9_-]*/)?(?:rules|signatures)/?$"),
    # Branch / git ref names quoted with slashes.
    re.compile(r"^chore/"),
    # State files written at runtime, not committed.
    re.compile(r"^state/"),
)

# Compile once.  PATH_RE matches any backtick-quoted token that
# contains at least one slash.
PATH_RE = re.compile(r"`([^`\s]+/[^`\s]+)`")

OK = "[OK  ]"
FAIL = "[FAIL]"


def _is_path_shape(target: str) -> bool:
    """True if target *looks* like a candidate file/dir reference.
    Filters out tool-arg shapes, glob patterns, and other obviously-
    not-a-path strings before we even try to resolve."""
    if any(c in target for c in "*?[]{}<>="):
        return False
    if "|" in target:
        return False
    # foo:bar where bar isn't a known file extension is probably
    # an arg shape (e.g. `case_id:str`, not `path:line`).
    if ":" in target and not any(
        target.endswith(ext)
        for ext in (
            ".md",
            ".py",
            ".rs",
            ".sh",
            ".toml",
            ".yml",
            ".yaml",
            ".json",
            ".txt",
            ".deb",
            ".evtx",
            ".log",
            ".jsonl",
            ".html",
            ".pdf",
            ".png",
        )
    ):
        return False
    return True


def _is_allowed(target: str) -> bool:
    """True if target matches one of the known false-positive
    shapes the audit should skip."""
    return any(p.match(target) for p in ALLOW_PATTERNS)


def _resolve(doc: str, target: str) -> Path | None:
    """Try resolution attempts in order. Returns the resolved
    Path if any of them exists, else None."""
    src_dir = (REPO / doc).parent
    candidates = [
        src_dir / target,
        REPO / target,
    ]
    if doc in SERVICE_PACKAGE_DIRS:
        candidates.append(REPO / SERVICE_PACKAGE_DIRS[doc] / target)
    for c in candidates:
        try:
            if c.exists():
                return c
        except OSError:
            continue
    return None


def _discover_docs() -> list[str]:
    """Return the sorted list of repo-relative doc paths to scan.
    Combines EXPLICIT_DOCS with GLOB_PATTERNS, minus GLOB_EXCLUDES.
    Glob-based so a new doc dropped under docs/, docs/runbooks/,
    agent-config/, or services/*/ is auto-gated next CI run."""
    seen: set[str] = set()
    for d in EXPLICIT_DOCS:
        if (REPO / d).exists():
            seen.add(d)
    for pat in GLOB_PATTERNS:
        for p in REPO.glob(pat):
            if not p.is_file():
                continue
            rel = p.relative_to(REPO).as_posix()
            if rel in GLOB_EXCLUDES:
                continue
            seen.add(rel)
    return sorted(seen)


def main() -> int:
    print("=" * 60)
    print("Find Evil! - path-existence-smoke")
    print("=" * 60)

    docs = _discover_docs()
    missing = []
    total_paths = 0
    for doc in docs:
        p = REPO / doc
        # _discover_docs only yields existing files; no need to
        # re-check existence here.

        text = p.read_text(encoding="utf-8")
        for m in PATH_RE.finditer(text):
            target = m.group(1).rstrip(".,;:?")
            if not _is_path_shape(target):
                continue
            if _is_allowed(target):
                continue
            total_paths += 1
            resolved = _resolve(doc, target)
            if resolved is None:
                line_no = text[: m.start()].count("\n") + 1
                missing.append((f"{doc}:{line_no}", target))

    print(f"checked {total_paths} backtick-quoted paths across {len(docs)} docs")
    print()

    if missing:
        for src, target in missing:
            print(f"{FAIL} {src}: {target!r}")
        print()
        print("=" * 60)
        print(f"FAIL - {len(missing)} broken path reference(s).")
        print("To fix: either correct the path or, if it's a")
        print("legitimate exception (e.g. a runtime install path,")
        print("a deferred-per-A2 future surface, or a")
        print("documented-and-quoted dropped module), add it to")
        print("ALLOW_PATTERNS in scripts/path-existence-smoke.py")
        print("with a one-line justification comment.")
        print("=" * 60)
        return 1

    print("=" * 60)
    print(f"OK - all {total_paths} path references resolve cleanly.")
    print("=" * 60)
    return 0


if __name__ == "__main__":
    sys.exit(main())
